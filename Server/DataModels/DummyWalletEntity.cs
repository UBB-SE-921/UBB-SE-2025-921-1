namespace Server.DataModels
{
    using System.ComponentModel.DataAnnotations.Schema;

    /// <summary>
    /// Represents the DummyWallet table structure for EF Core mapping.
    /// OBS: This is the exact same table as Maria had it, it has no foreign keys to any buyers, nothing :)).
    /// Change if needed.
    /// </summary>
    [Table("DummyWallets")]
    public class DummyWalletEntity
    {
        /// <summary>
        /// Initializes a new instance of the <see cref="DummyWalletEntity"/> class.
        /// </summary>
        /// <param name="balance">The balance of the wallet.</param>
        public DummyWalletEntity(float balance)
        {
            this.ID = 0; // (default int value) this should be automatically generated by the DB
            this.Balance = balance;
        }

        /// <summary>
        /// Gets or sets the ID of the wallet.
        /// </summary>
        public int ID { get; set; }

        /// <summary>
        /// Gets or sets the balance of the wallet.
        /// </summary>
        public float Balance { get; set; }
    }
}